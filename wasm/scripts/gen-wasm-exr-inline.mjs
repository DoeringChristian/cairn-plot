// Package the wasm-pack output (wasm/exr-decode/pkg/) into a COMMITTED,
// self-contained TS module under wasm/exr-decode/inline/ so downstream
// consumers never need cargo/wasm-pack (same policy as dist/_assets):
//
//   inline/
//     cairn_exr_decode.js    (copied wasm-bindgen glue, --target web)
//     cairn_exr_decode.d.ts  (copied types)
//     wasm-exr-inline.ts     (GENERATED: WASM_EXR_B64 + loadExrDecoder())
//
// The .wasm bytes are embedded as base64 and instantiated via
// `WebAssembly.instantiate(bytes)` (the glue's default init takes a BufferSource
// straight to instantiate — NO fetch), so it is CSP- and file://-safe and ships
// inside the self-contained plot pages.
//
// Run after every `wasm-pack build`:  node wasm/scripts/gen-wasm-exr-inline.mjs

import { readFileSync, writeFileSync, copyFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const crate = resolve(here, "../exr-decode");
const pkg = resolve(crate, "pkg");
const outDir = resolve(crate, "inline");

mkdirSync(outDir, { recursive: true });

// 1. Copy the wasm-bindgen glue + types verbatim (committed).
copyFileSync(resolve(pkg, "cairn_exr_decode.js"), resolve(outDir, "cairn_exr_decode.js"));
copyFileSync(resolve(pkg, "cairn_exr_decode.d.ts"), resolve(outDir, "cairn_exr_decode.d.ts"));

// 2. Base64-embed the .wasm.
const wasm = readFileSync(resolve(pkg, "cairn_exr_decode_bg.wasm"));
const b64 = wasm.toString("base64");

const ts = `/**
 * GENERATED by wasm/scripts/gen-wasm-exr-inline.mjs — DO NOT EDIT.
 *
 * Self-contained inline OpenEXR WASM decoder for cairn-plot. The .wasm bytes are
 * embedded as base64 and instantiated via \`WebAssembly.instantiate(bytes)\` (no
 * fetch, CSP/file:// safe). Committed so consumers need no cargo/wasm-pack.
 *
 * .wasm size: ${wasm.length} bytes (base64: ${b64.length} chars).
 *
 * Usage:
 *   const { decode_exr } = await loadExrDecoder();
 *   const img = decode_exr(new Uint8Array(exrBytes));
 *   // img.precision === "f16-bits" ? img.halfBits : img.floats
 */
import initWasm, { decode_exr, DecodedImage } from "./cairn_exr_decode.js";

/** The release .wasm, base64-encoded. */
export const WASM_EXR_B64 =
  "${b64}";

/** Decode a base64 string into raw bytes (browser + node, no Buffer/fetch). */
function base64ToBytes(b64: string): Uint8Array {
  const bin = atob(b64);
  const out = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i);
  return out;
}

let ready: Promise<void> | null = null;

/**
 * Instantiate the WASM decoder once (idempotent) from the embedded bytes and
 * return the decode entry point. No network / filesystem access.
 */
export async function loadExrDecoder(): Promise<{
  decode_exr: typeof decode_exr;
  DecodedImage: typeof DecodedImage;
}> {
  if (!ready) {
    // Passing a BufferSource routes the glue straight to WebAssembly.instantiate
    // (it only fetch()es for string/URL/Request inputs) — no network.
    ready = initWasm({ module_or_path: base64ToBytes(WASM_EXR_B64) }).then(() => {});
  }
  await ready;
  return { decode_exr, DecodedImage };
}
`;

writeFileSync(resolve(outDir, "wasm-exr-inline.ts"), ts);

console.log(
  `wrote inline module: ${resolve(outDir, "wasm-exr-inline.ts")}\n` +
    `  .wasm ${wasm.length} bytes -> base64 ${b64.length} chars`,
);
